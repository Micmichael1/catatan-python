# Mengeluarkan semua isi dari array nama
nama=['Michael','Lim']
for item in nama:
    print(item)
# Mengeluarkan semua isi dari array angka
angka=[1,2,3]
for item in angka:
    print(item)

# Mulai dari 0 sampai 4
for item in range (5):
    print(item)

# Mulai dari 1 sampai 4
for item in range(1,5):
    print(item)

# Mulai dari 1 sampai 9 dengan beda 2
for item in range (1,10,2):
    print(item)

