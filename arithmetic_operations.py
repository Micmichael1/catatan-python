print(type(angka1))
print(10%3)

# increment cara pertama
x=10
x=x+3
print(x)

# increment cara kedua (augmented assignment operator)
y=10
y+=3
print(y)

# decrement cara pertama
x=10
x=x-3
print(x)

# decrement cara kedua (augmented assignment operator)
y=10
y-=3
print(y)