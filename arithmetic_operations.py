print(10+3)
print(10-3)
print(10*3)
# Mengeluarkan hasil 2 pangkat 3
print(2**3)
# Mengeluarkan hasil bagi berupa tipe data float
print(10/3)
print(round(10/3,3))
print(format(10/3,".3f"))
print("{:.3f}".format(10/3))
# Mengeluarkan hasil bagi berupa tipe data int
print(10//3)
angka=10/3
angka1=10//3
