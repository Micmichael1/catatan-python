# Urutan prioritas :
# 1.Pangkat (**)
# 2.Perkalian (*) dan Pembagian (/)
# 3.Penjumlahan (+) dan Pengurangan (-)

# Untuk Memprioritaskan bisa menggunakan parenthesis yaitu ()
y = (10 + 3) * 2 ** 2
# Maka 10+3 akan dikerjakan duluan karena mengandung parenthesis ()
print(y)