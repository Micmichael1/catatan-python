import math


# Fungsi math.ceil() dan math.floor() sama sama memerlukan module math

# Fungsi Membulatkan Ke Atas Menggunakan Fungsi math.ceil()
print(math.ceil(1.23456))
# Fungsi Membulatkan Ke Bawah Menggunakan Fungsi math.floor()
print(math.floor(1.23456))




# Fungsi round() dan abs() tidak memerlukan module math

# Fungsi Pembulatan Keatas Menggunakan Fungsi Round()
x=2.9
print(round(x))
y=2.9876
print(round(y,2))

# Fungsi Mengubah Menjadi Bilangan Positif Menggunakan Fungsi abs()
z=-2.9
print(abs(z))
